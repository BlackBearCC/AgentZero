      textShadow: `0 0 ${3 + normalizedWeight * 7}px rgba(68, 255, 68, ${0.5 + normalizedWeight * 0.5})`
    };
    
    result.push({
      text,
      count,
      style,
      weight: normalizedWeight
    });
  }
  
  return result;
}

